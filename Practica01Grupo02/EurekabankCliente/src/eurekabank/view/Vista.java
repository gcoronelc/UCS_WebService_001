    private void btnMovimientosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMovimientosActionPerformed
        // TODO add your handling code here:
        String numeroCuenta = txtNumeroCuenta.getText();
        CuentaService cuenta = new CuentaService();
        
        List<Movimiento> movimientos = new ArrayList<Movimiento>();
        
        movimientos = cuenta.leerMovimientos(numeroCuenta);
        
        // Acceso al objeto Table
        DefaultTableModel tabla;
        tabla = (DefaultTableModel) tblMovimiento.getModel();
        
     // Eliminar  todas las filas
        tabla.setRowCount(0);
        
     // Cargar Datos
        for (Movimiento t : movimientos) {
          Object[] rowData = {
            t.getNumeroMovimiento(),
            t.getFecha(),
            t.getTipo(),
            t.getAccion(),
            t.getImporte()
          };
          tabla.addRow(rowData);
        }
        
    }//GEN-LAST:event_btnMovimientosActionPerformed

