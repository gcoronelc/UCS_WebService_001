public class Cuenta {

  private String sucucodigo;
  private String sucunombre;
  private String cliecodigo;
  private String cliepaterno;
  private String cliematerno;
  private String clienombre;
  private String cuencodigo;
  private Double cuensaldo;
  private String cuenestado;
  private String monecodigo;
  private String monenombre;

  public Cuenta() {
  }

  public String getSucucodigo() {
    return sucucodigo;
  }

  public void setSucucodigo(String sucucodigo) {
    this.sucucodigo = sucucodigo;
  }

  public String getSucunombre() {
    return sucunombre;
  }

  public void setSucunombre(String sucunombre) {
    this.sucunombre = sucunombre;
  }

  public String getCliecodigo() {
    return cliecodigo;
  }

  public void setCliecodigo(String cliecodigo) {
    this.cliecodigo = cliecodigo;
  }

  public String getCliepaterno() {
    return cliepaterno;
  }

  public void setCliepaterno(String cliepaterno) {
    this.cliepaterno = cliepaterno;
  }

  public String getCliematerno() {
    return cliematerno;
  }

  public void setCliematerno(String cliematerno) {
    this.cliematerno = cliematerno;
  }

  public String getClienombre() {
    return clienombre;
  }

  public void setClienombre(String clienombre) {
    this.clienombre = clienombre;
  }

  public String getCuencodigo() {
    return cuencodigo;
  }

  public void setCuencodigo(String cuencodigo) {
    this.cuencodigo = cuencodigo;
  }

  public Double getCuensaldo() {
    return cuensaldo;
  }

  public void setCuensaldo(Double cuensaldo) {
    this.cuensaldo = cuensaldo;
  }

  public String getCuenestado() {
    return cuenestado;
  }

  public void setCuenestado(String cuenestado) {
    this.cuenestado = cuenestado;
  }

  public String getMonecodigo() {
    return monecodigo;
  }

  public void setMonecodigo(String monecodigo) {
    this.monecodigo = monecodigo;
  }

  public String getMonenombre() {
    return monenombre;
  }

  public void setMonenombre(String monenombre) {
    this.monenombre = monenombre;
  }

}
